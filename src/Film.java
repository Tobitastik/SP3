import java.util.ArrayList;

public class Film {

    public String name;
    public int year;
    public ArrayList<String> cat;
    public double rating;

    public String getName() {
        return name;
    }

    public int getYear() {
        return year;
    }

    public ArrayList<String> getCat() {
        return cat;
    }

    public double getRating() {
        return rating;
    }
}
